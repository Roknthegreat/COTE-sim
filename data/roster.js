// ── Talent System Rule ─────────────────────────────────────
// TAL (Talent) boosts growth rate for: INT, CMB, CHA, PER, STL, WIL
// TAL does NOT affect PHY — raw physical strength grows only through dedicated training.
const TALENT_AFFECTED_STATS = ["INT", "CMB", "CHA", "PER", "STL", "WIL"];
const TALENT_EXEMPT_STATS = ["PHY"]; // Physical strength is immune to Talent boosts

// ── NPC Stat Cap Note ─────────────────────────────────────
// Most NPCs are capped in their primary stat at ~83-85.
// Achieving 90+ in any stat is reserved for the player through late-game TAL compounding.
// PHY 80+ and CMB 80+ NPCs represent the hardest fighters in school.
// No NPC has all stats above 70 simultaneously (forcing meaningful tradeoffs).
const NPC_STAT_SOFT_CAP = 85;
const PLAYER_STAT_SOFT_CAP = 100;

// ── Helpers ───────────────────────────────────────────────

function getRosterByClass(cls) {
  return STUDENT_ROSTER.filter(s => s.class === cls);
}

function getStudentById(id) {
  return STUDENT_ROSTER.find(s => s.id === id);
}

function getTalentGrowthMultiplier(stat, talentValue) {
  if (TALENT_EXEMPT_STATS.includes(stat)) return 1.0;
  return 1.0 + (talentValue / 100) * 0.8; // TAL 100 → 1.8x growth, TAL 50 → 1.4x
}

function initRosterRelationships(playerId) {
  STUDENT_ROSTER.forEach(s => {
    if (!s.relationships[playerId]) {
      s.relationships[playerId] = 0;
    }
  });
}
